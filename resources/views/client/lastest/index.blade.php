                                            <p></p>
                                            <p><em>Although your friends are envious of your voluminous hair, some mornings
                                                    you would gladly trade it in for flat locks. Dryness, frizz, untameable
                                                    volume... It does come with minor annoyances that makes your life
                                                    difficult. However, rest assured, your hair is not a lost cause. There
                                                    are lots of tips to get your thick hair back in tip-top condition and
                                                    full of vitality once more.</em></p>
                                            <p>Start by choosing a haircut suited to your hair type. Say goodbye to
                                                ultra-short haircuts at the risk of ending up with a rather unflattering
                                                "helmet hair" effect, and layers which pump up the volume. Instead opt for
                                                thinned-out haircuts, which are ideal for reducing the thickness. Would you
                                                like a fringe? No problem, as long as it helps reduce the volume of the
                                                whole hairstyle, such as a curtain fringe. Once styled to your satisfaction,
                                                it's time to take care of your thick hair, which generally tends to be very
                                                dry. Try-out detangling products, masks and other nourishing treatments. If
                                                despite their density your locks are not lacking in hydration, use pH
                                                neutral shampoos and conditioners. The important thing is to tailor your
                                                haircare regime to your hair type. If you have thick hair, there is always
                                                one saying: be generous with the amount of product you apply so you can
                                                properly spread it out over the entirety of your hair. The very important
                                                final step: detangling. Due to its high density, thick hair is prone to
                                                knots. A wide tooth comb will help you do the job much more easily.</p>
                                            <p>In this feature article you will find even more advice on how to enhance your
                                                thick hair.</p>
