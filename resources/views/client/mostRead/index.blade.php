<h1>How to apply a hair mask correctly</h1>
                <div class="content tracking-click-parent" data-tracking-category="Contenu">

                                                            Whether you have coloured, dry, damaged or fragile hair, it is important to use the right product to repair and nourish from the roots to the ends. A hair mask doesn’t necessarily have to be something you use every once in a while: why not make it a part of your hair routine? Here’s our advice on how to use it.<br><strong>
</strong><br>A hair mask should be applied to washed and towel-dried hair. “Spread the cream evenly throughout your hair, piece by piece. Massage the product in from the roots to the tips then comb through your hair with your fingers.” To optimise the mask’s effects, cover your head with a hot towel for at least 10 minutes. That way, the mask is in direct contact with your scalp so it can be absorbed more easily. “Leave the mask on for up to 20 minutes, depending on your hair’s condition.” Lather with cool water before rinsing gently.<br>&nbsp;<br>How often you should use a hair mask depends on your hair type. “Ideally, you should use it every time you wash your hair, but once a week is OK. Either way, alternate with leave-in treatments to untangle your hair in no time.” If you have really dry or frizzy hair, you will need to give it lots of TLC! We therefore advise you to apply a hair mask regularly and leave it on overnight wrapped in a towel. When you wake up, your hair will be silky and soft.<br><strong>
</strong><br><strong>Our tip</strong>: Avoid combing your hair once you have applied the mask; let it absorb everything it needs to restore itself.<br>&nbsp;<br>© DR © Jean Louis David
