                            <h1>Men: what are the on trend haircuts from the JLD Spring-Summer 2016 Collection?</h1>
                            <div class="content tracking-click-parent" data-tracking-category="Contenu">

                                <p><em>Gentlemen, do you wish to boost your look by changing your hairstyle? Feel free to be
                                        inspired by the new Jean Louis David haircut from the Spring-Summer 2016 Collection!
                                        We take a closer look.</em></p>
                                <h2>Do men follow trends as much as women?</h2>
                                <p>"Yes, of course. For several years, we have noticed that men are very active as far as
                                    trends and hairstyles are concerned. They pay particular attention to the image they
                                    reflect. In addition, men are increasingly present in our salons. Therefore, there is a
                                    real demand. Each season, we systematically offer hairstyles which closely follow trends
                                    and which are likely to suit them."</p>
                                <blockquote class="epigraph">"Today, men represent practically 50% of our clientele."
                                </blockquote>
                                <p><span class="author">Luis Faria</span></p>
                                <div class="fotoBlock">
                                    <picture>
                                        <img src="https://via.placeholder.com/634x1031"
                                            alt="Men's haircut – New collection"> </picture>
                                    <div class="text">Men's haircut – New collection</div>
                                </div>
                                <h2>Take a closer look at the haircut from the new collection </h2>
                                <p>"It's a mid-length haircut, full of movement which preserves the hair's volume. The sides
                                    are short, however there is still substance on top of the head. The hair is slightly
                                    layered. As the theme of the new collection is sport, we wanted an athletic model with a
                                    dynamic and modern haircut. However, the haircut is not pronounced. When it's not
                                    styled, it has a rather undefined and highly adaptable finish".</p>
                                <div class="fotoBlock">
                                    <picture>
                                        <img src="https://via.placeholder.com/634x1031"
                                            alt="Men's haircut"> </picture>
                                    <div class="text">Men's haircut</div>
                                </div>
                                <h2>What are the possible hairstyles for this haircut?</h2>
                                <p>"Whether you have stylish or straightened hair, you prefer a natural effect, flattened
                                    down locks or a retro look... this haircut allows you to have a multitude of different
                                    hairstyles! As a result, it adapts easily. In fact, we have chosen not to give a strong
                                    character to the haircut. We preferred to offer a blank page on which everyone can
                                    express themselves. Whatever your look, you can adapt it, personalise it and express
                                    yourself <a
                                        href="//www.jeanlouisdavid.us/article/essential-styling-products-for-men_a5313/1"
                                        target="_blank" rel="nofollow">according to the hairstyle</a> . This season, the
                                    only limit is your imagination!"</p>
