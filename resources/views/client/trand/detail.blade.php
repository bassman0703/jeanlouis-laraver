                            <h1>Men: the influence of the fifties on the Spring-Summer 2020 hairstyles</h1>
                            <div class="content tracking-click-parent" data-tracking-category="Contenu">

                                <p>Gentlemen, this summer is the time to say goodbye to bohemian styles, rock looks and man
                                    buns. Now Jean Louis David's Spring-Summer collection injects a touch of 1950s
                                    sophistication and glamour into current trends. Think Paul Newman's wild style or James
                                    Dean and Elvis Presley's sex appeal: this season you can give the 50s look a go for
                                    yourself!</p>
                                <h2>Crazy about JLD: a taste for revival</h2>
                                <p>This season, Jean Louis David wants to pay homage to some of the most legendary,
                                    glamourous and rock'n'roll looks from past decades to create on-trend styles for both
                                    men and women. More specifically, the retro-chic look from the 50s, which has inspired
                                    the masculine looks for the brand new Crazy about JLD collection...</p>
                                <h2>A James Dean-style retro cut</h2>
                                <p>You must admit, you couldn't honestly say you would turn down a look inspired by the
                                    incredibly famous James Dean! Based on the same principle as the highly on-trend
                                    undercut (very short hair on the sides and volume on top) this 50s cut preserves a
                                    similar distribution of volume but with more hair, notably on the sides.</p>
                                <div class="fotoBlock">
                                    <picture>
                                        <img src="https://via.placeholder.com/634x728"
                                            alt="Style like Elvis Presley"> </picture>
                                    <div class="text">Style like Elvis Presley</div>
                                </div>
                                <h2>Gentlemen, dare to give blow-drying a try!</h2>
                                <p>To ensure you create enough volume and to give hold and style to your 50s look, you will
                                    most likely need to blow-dry your hair. To do this, get your hands on a round brush with
                                    an average-sized diameter, (suited to your hair length) then dry your hair. Brush it out
                                    towards the back to obtain wonderful movement and to lift the hair at the roots. If your
                                    hair is still not tamed, despite the blow-drying, don't panic. You can put it right with
                                    this step!</p>
                                <h2>Slicked-back like Elvis Presley...</h2>
                                <p>Once your hair is long enough to reinterpret this famous perfectly blow-dried 50s look,
                                    all you need to do is give the look hold and the famous sought-after 'slicked back'
                                    effect. Use a fine-toothed comb, Jean Louis David's Go Style Sculpting Paste and Fix
                                    Ultime Go Style spray. Apply a few squirts of Sculpting Paste into the palm of your
                                    hand, heat it up and apply it to your hair, taking care to sweep it towards the back
                                    without destroying the quiff. For more definition, you can use a comb to help. To ensure
                                    your hairstyle has flawless hold, apply Fix Ultime extra hold hairspray. Ta-da, you're
                                    good to go!</p>
