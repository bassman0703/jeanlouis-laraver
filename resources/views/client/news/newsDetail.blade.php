                            <h1>Looks from the Crazy about JLD collection</h1>
                            <div class="content tracking-click-parent" data-tracking-category="Contenu">

                                <p>With the Spring-Summer 2020 Crazy about JLD collection, Jean Louis David pays homage to
                                    some of the most memorable hairstyles from past decades. From the 50s to the 90s,
                                    quiffs, mullets and cult looks which went down in history, these retro hairstyles are
                                    now making a huge comeback without having aged a single day!</p>
                                <h2>90s layers</h2>
                                <p>In the fashion world, the 90s have made a huge comeback in recent years. However, layers
                                    never really followed this trend. As a result, Jean Louis David wanted to give layers
                                    the comeback they deserve by making them one of the key looks of the brand's latest
                                    collection. Shorter and more structured than the initial 90s version with a rock vibe,
                                    the 2020 layered style looks just as fabulous whether straightened, blow-dry styled or
                                    curled.</p>
                                <h2>The 70s mullet cut</h2>
                                <div class="fotoBlock">
                                    <picture>
                                        <img src="https://via.placeholder.com/610x365"
                                            alt="The mullet"> </picture>
                                    <div class="text">The mullet</div>
                                </div>
                                <p>A highly on-trend must-have ultra-rock look, the mullet is by far the strongest cut from
                                    the Crazy about JLD collection. Equally stylish whether straight or curled, this
                                    hairstyle is sure to be a favourite amongst fearless fashionistas.</p>
                                <h2>The 70s curly bob</h2>
                                <div class="fotoBlock">
                                    <picture>
                                        <img src="https://via.placeholder.com/610x365"
                                            alt="The seductive bob"> </picture>
                                    <div class="text">The seductive bob</div>
                                </div>
                                <p>Extremely glamourous, the Jean Louis David 70s style bob with XXL curls definitely has
                                    all the makings of becoming a cult look once more!</p>
                                <h2>The seductive bob</h2>
                                <div class="fotoBlock">
                                    <picture>
                                        <img src="https://via.placeholder.com/610x365"
                                            alt="The blow-dried bob"> </picture>
                                    <div class="text">The blow-dried bob</div>
                                </div>
                                <p>Probably the most timeless look from this Spring-Summer collection, this Jean Louis David
                                    mid-length bob is slightly layered at the tips to add extra movement. Whether
                                    straightened, blow-dried or curled, this style is sure to seduce those who love elegant
                                    looks.</p>
                                <h2>A 50s-inspired look for men</h2>
                                <div class="fotoBlock">
                                    <picture>
                                        <img src="https://via.placeholder.com/610x365"
                                            alt="5O's inspired looks for men"> </picture>
                                    <div class="text">5O's inspired looks for men</div>
                                </div>
                                <p>When it comes to men, the 50s are the key decade for this collection. Hair styled towards
                                    the back, XL quiffs and slicked-back effects, retro looks have never been so sexy or
                                    looked this cool!</p>
