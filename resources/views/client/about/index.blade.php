                                <h2>History</h2>
                                <p>"Being current and taking pleasure in reflecting his clientele's evolving tastes and
                                    underlying desires." This is Jean Louis David's mantra. The visionary hairdresser opened
                                    his first salon in 1961 in Avenue Wagram in Paris, and it wasn't long before he was at
                                    the top of his profession. This success was largely due to his innovative techniques and
                                    his passion for woman's beauty. For Jean Louis David, hairdressing is an art which he
                                    continually reinvents as the seasons change.</p>
                                <p>After working with some of the greatest photographers in the 1960s – including Helmut
                                    Newton and Herb Ritts – Jean Louis David decided to get behind the camera, video and
                                    photo, himself in the 1970s. This allowed him to display a large number of hairstyles to
                                    his clients so they could choose which one they wanted. At the time, it was Jean Louis
                                    David himself who was in charge of the changing designs. Since his departure, an
                                    artistic team has taken over, keeping up with the latest trends while continuing to
                                    invent particular cuts, colours and hairstyles for the sake of women's pleasure.</p>
                                <p>Jean Louis David has thus continuously renewed himself in order to reinvent the beauty of
                                    hair. We owe him particularly for the famous layered cut, realised for the first time in
                                    1970, originally created to free women from the shackles of rigid cuts. Today, the
                                    artistic team continues this tradition and respects this heritage by proposing easy
                                    going modern cuts, with customised natural colours.</p>
                                <p>Building on his success in the media and in the world of business, Jean Louis David has
                                    built an empire by opening a series of franchise salons in his name. For him, this
                                    "allows traders to profit from the reputation of a franchise and to access a host of
                                    commercial methods and techniques specially developed for these franchisees." This
                                    gamble paid off as his network has grown significantly over the years; there are now
                                    over a thousand Jean Louis David salons across the world. It remains the number one
                                    hairdressing network in Europe and the second in the world.</p>
                                <h2>Training</h2>
                                <p>In order to ensure that the stylists at Jean Louis David are always at the forefront of
                                    the latest trends and techniques, Jean Louis David has made training a priority. At the
                                    end of the 1970s, he came up with video training to ensure his stylists have the key to
                                    his unique know-how, identical in all his salons.</p>
                                <p>Today, to communicate this know-how, there is a dedicated Training Centre based in rue
                                    Cambon in Paris, where all the stylists in the network come to build on their expertise
                                    and learn the latest trends in hair styling, new colouring techniques and how to use the
                                    brand's products. The centre, which covers 350m2, offers several levels of training;
                                    introductory modules for newcomers, themed training courses for more experienced staff
                                    as well as specific training for salon managers.</p>
                                <h2>A unique salon experience</h2>
                                <h3>The world of Jean Louis David salons</h3>
                                <p>Light, modern and attractive colours mean that Jean Louis David salons feel clean and
                                    contemporary. Warm materials, such as wood, furnish the rounded curves of the salons,
                                    giving them a feminine touch. The ergonomic chairs, the lighting and even the shape of
                                    the dressing tables have all been designed with a single goal: to provide the maximum
                                    comfort to customers. This design promotes the unique atmosphere of Jean Louis David
                                    salons, an atmosphere which perfectly balances elegance and moderation with modernity
                                    and technicality.</p>
                                <h3>The quality service of Jean Louis David</h3>
                                <p>Welcoming, listening, caring...the Jean Louis David experience is one of pure relaxation.
                                    The stylists do their utmost to ensure an excellent quality of service so that each
                                    visit to the salon is a unique one. The result of an attitude towards customers for
                                    which Jean Louis David has been renown since the creation of his salons!</p>
                                <h3>A watchword: a personalised experience</h3>
                                <p>So that every haircut is unique, each visit to the salon begins with an individual and
                                    personalised discussion, known as the "Collection Moment" during which the customer,
                                    along with the stylist, selects her favourite hairstyles from the books exclusive to
                                    Jean Louis David. The stylist will then decide on what is or is not possible, based on
                                    the nature of the hair and each customer's particular style.</p>
